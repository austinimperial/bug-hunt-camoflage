// matches a list of rgb colors in the form "[122,34,45], [2,255,56] ..."
export const colorListRegExp = new RegExp(
  /^\s*(\[(([0-9]|[0-9][0-9]|[0-1][0-9][0-9]|[0-2][0-5][0-5]),){2}([0-9]|[0-9][0-9]|[0-1][0-9][0-9]|[0-2][0-5][0-5])\]\s*,\s*){0,}\[(([0-9]|[0-9][0-9]|[0-1][0-9][0-9]|[0-2][0-5][0-5]),){2}([0-9]|[0-9][0-9]|[0-1][0-9][0-9]|[0-2][0-5][0-5])\]\s*,*\s*$/,
  "g"
);


